print("# Valores de range(5):")
for n in range(5):
	print(n)

print("\n# Valores de range(3, 8):")
for n in range(3, 8):
	print(n)

print("\n# Valores de range(8, 16, 2):")
for n in range(8, 16, 2):
	print(n)

L = [5, 25, 125, 625, 3125]
print("\n# Valores da lista {}:".format(L))
for n in L:
	print(n)